package io.cristian.pruebasunitarias;

public class Operaciones {

    public int sumar(int valorUno, int valorDos){
        return valorUno + valorDos;
    }
    public int restar(int valorUno, int valorDos){
        return valorUno - valorDos;
    }

    public int multiplicar(int valorUno, int valorDos){
        return valorUno * valorDos;
    }

    public int dividir(int valorUno, int valorDos){
        return valorUno / valorDos;
    }
}
